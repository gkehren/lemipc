#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*car;

	i = 0;
	car = (char *)s;
	while (i < (int)n)
	{
		car[i] = '\0';
		i++;
	}
	s = (void *)car;
}
